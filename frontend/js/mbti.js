// 计算MBTI结果
function calculateMBTIResult(answers) {
  // 统计各维度得分
  const counts = {
    e: 0, i: 0,
    s: 0, n: 0,
    t: 0, f: 0,
    j: 0, p: 0
  };

  // 每个维度的问题数量
  const dimensionCounts = {
    e: 10, i: 10,
    s: 20, n: 20,
    t: 20, f: 20,
    j: 20, p: 20
  };

  // 统计每个维度的得分，确保不超过对应维度的问题数量
  answers.forEach(answer => {
    if (counts.hasOwnProperty(answer) && counts[answer] < dimensionCounts[answer]) {
      counts[answer]++;
    }
  });

  // 计算百分比
  const percentages = {
    e: Math.min((counts.e / 10) * 100, 100),
    i: Math.min(100 - (counts.e / 10) * 100, 100),
    s: Math.min((counts.s / 20) * 100, 100),
    n: Math.min(100 - (counts.s / 20) * 100, 100),
    t: Math.min((counts.t / 20) * 100, 100),
    f: Math.min(100 - (counts.t / 20) * 100, 100),
    j: Math.min((counts.j / 20) * 100, 100),
    p: Math.min(100 - (counts.j / 20) * 100, 100)
  };

  // 确定类型
  const type = [
    counts.e > counts.i ? 'E' : 'I',
    counts.s > counts.n ? 'S' : 'N',
    counts.t > counts.f ? 'T' : 'F',
    counts.j > counts.p ? 'J' : 'P'
  ].join('');

  // 计算认知功能值
  const cognitiveFunctions = {
    Ni: Math.round((percentages.i * percentages.n) / 100),
    Ne: Math.round((percentages.e * percentages.n) / 100),
    Si: Math.round((percentages.i * percentages.s) / 100),
    Se: Math.round((percentages.e * percentages.s) / 100),
    Ti: Math.round((percentages.i * percentages.t) / 100),
    Te: Math.round((percentages.e * percentages.t) / 100),
    Fi: Math.round((percentages.i * percentages.f) / 100),
    Fe: Math.round((percentages.e * percentages.f) / 100)
  };

  // 功能栈映射
  const functionStacks = {
    'INTJ': ['Ni', 'Te', 'Fi', 'Se'],
    'INTP': ['Ti', 'Ne', 'Si', 'Fe'],
    'ENTJ': ['Te', 'Ni', 'Se', 'Fi'],
    'ENTP': ['Ne', 'Ti', 'Fe', 'Si'],
    'INFJ': ['Ni', 'Fe', 'Ti', 'Se'],
    'INFP': ['Fi', 'Ne', 'Si', 'Te'],
    'ENFJ': ['Fe', 'Ni', 'Se', 'Ti'],
    'ENFP': ['Ne', 'Fi', 'Te', 'Si'],
    'ISTJ': ['Si', 'Te', 'Fi', 'Ne'],
    'ISFJ': ['Si', 'Fe', 'Ti', 'Ne'],
    'ESTJ': ['Te', 'Si', 'Ne', 'Fi'],
    'ESFJ': ['Fe', 'Si', 'Ne', 'Ti'],
    'ISTP': ['Ti', 'Se', 'Ni', 'Fe'],
    'ISFP': ['Fi', 'Se', 'Ni', 'Te'],
    'ESTP': ['Se', 'Ti', 'Fe', 'Ni'],
    'ESFP': ['Se', 'Fi', 'Te', 'Ni']
  };

  // 获取当前类型的功能栈
  const functionStack = functionStacks[type] || ['Ni', 'Te', 'Fi', 'Se'];

  return {
    type,
    counts,
    percentages,
    cognitiveFunctions,
    functionStack,
    typeInfo: MBTI_DATA[type] || {
      name: '未知类型',
      description: '请重新测试',
      category: '未知'
    }
  };
}

