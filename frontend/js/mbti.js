// 绘制雷达图
function drawRadarChart(result) {
  try {
    const canvas = document.getElementById('radarChart');
    if (!canvas) {
      console.error('Canvas 元素不存在');
      return;
    }

    const ctx = canvas.getContext('2d');
    const labels = ['外向性', '感知', '思考', '判断', '内向性', '直觉', '感受', '感知'];
    
    // 准备数据
    const data = [
      result.percentages.e,
      result.percentages.s,
      result.percentages.t,
      result.percentages.j,
      result.percentages.i,
      result.percentages.n,
      result.percentages.f,
      result.percentages.p
    ];

    // 销毁已存在的图表（如果有）
    if (window.radarChartInstance) {
      window.radarChartInstance.destroy();
    }

    // 绘制新的雷达图
    window.radarChartInstance = new Chart(ctx, {
      type: 'radar',
      data: {
        labels: labels,
        datasets: [{
          label: '你的得分',
          data: data,
          borderColor: '#FFB6C1',
          backgroundColor: 'rgba(255, 182, 193, 0.2)',
          borderWidth: 2,
          pointBackgroundColor: '#FFB6C1',
          pointBorderColor: '#fff',
          pointBorderWidth: 2,
          pointRadius: 5,
          pointHoverRadius: 7,
          fill: true
        }]
      },
      options: {
        responsive: true,
        maintainAspectRatio: false,
        scales: {
          r: {
            beginAtZero: true,
            max: 100,
            ticks: {
              stepSize: 20,
              display: false,
              color: '#999',
              font: {
                size: 12
              }
            },
            grid: {
              display: true,
              color: 'rgba(0, 0, 0, 0.1)'
            }
          }
        },
        plugins: {
          legend: {
            display: true,
            labels: {
              color: '#5a5a5a',
              font: {
                size: 16,
                weight: '600'
              },
              padding: 15
            }
          },
          filler: {
            propagate: true
          }
        }
      }
    });
  } catch (error) {
    console.error('绘制雷达图失败:', error);
  }
}

